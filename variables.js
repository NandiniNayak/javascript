// declare a variable in js is using a keyword

var name = "BOB";   // declaration 
name = "Derek";   //assignment
console.log(name);

// const is also a block scoped variable
const pi = 3.14;
console.log(pi);

// ES6 onwards a new keyword was introduced called let
// let is a block scoped variable
let age = 50;
console.log(age);

// block is confined to curly braces
{
    let name = "DEREK";
}
console.log(name);

